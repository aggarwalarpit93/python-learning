
# first need to install the virtual environment - pip install virtualenv
# Now can create multiple envs - python -m venv ENV_NAME
# To activate the specific env - source ENV_NAME/bin/activate
# To deactivate or come out of virtualenv - deactivate
# To check all the packages installed in an environment - pip3 list
# To generate requirements.txt file - pip freeze > requirements.txt


# Now all the dependencies installed after activating will be specific to this env only
